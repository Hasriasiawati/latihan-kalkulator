<?php

	function penjumlahan($x, $y) {
		$hasil = $x + $y;
		return $hasil;
	}

	function pengurangan($x, $y) {
		$hasil = $x - $y;
		return $hasil;
	}

	function perkalian($x, $y) {
		$hasil = $x * $y;
		return $hasil;
	}

	function pembagian($x, $y) {
		$hasilx = $x / $y;
		$hasil = round($hasilx, 2);

		return $hasil;

		
	}

?>